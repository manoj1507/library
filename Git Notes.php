Git Notes

git add . -> adds everything that is uncommited to the git database
git commit -> helps you save your work in local database
	git commit -m "Your commit message" -> is the full command
git push origin master -> Pushes your commit to Github
	origin -> is the location of the repository
	master -> is the branch name
git status -> Tells you if there is anything to commit
git log => Shows you the log of commits you have made